import { OpenInNew } from "@mui/icons-material";
import {
  Divider,
  List,
  ListItem,
  ListItemButton,
  ListItemIcon,
  ListItemText,
} from "@mui/material";
import { Inter } from "@next/font/google";
import { InferGetServerSidePropsType } from "next";

import Head from "next/head";
import Link from "next/link";

const inter = Inter({ subsets: ["latin"] });

type TPopularMovie = {
  id: number;
  title: string;
  overview: string;
  release_date: string;
  vote_average: number;
};

type TPopularResponse = {
  page: number;
  results: TPopularMovie[];
  total_pages: number;
  total_results: number;
};

export const getServerSideProps = async () => {

  const response = await fetch(
    `https://api.themoviedb.org/3/movie/popular?api_key=${process.env.API_KEY}`
  );

  const data = await response.json();

  return {
    props: {
      data,
    },
  };
};

export default function Home({
  data,
}: InferGetServerSidePropsType<typeof getServerSideProps>) {
  return (
    <>
      <Head>
        <title>My Next Movie</title>
        <meta name="description" content="Generated by create next app" />
        <meta name="viewport" content="width=device-width, initial-scale=1" />
        <link rel="icon" href="/favicon.ico" />
        <meta name="author" content="Guilherme Daniel" />
        <meta name="description" content="Made with next.js" />
      </Head>
      <main>
        <h1>Filmes Populares</h1>

        <List>
          {data?.results?.map((item: TPopularMovie) => (
            <>
              <ListItem
                key={item.id}
                sx={{
                  width: "100%",
                  maxWidth: 360,
                  bgcolor: "background.paper",
                }}
              >
                <ListItemText primary={`${item.title}`} />
                <Link href={`/movie/${item.id}`}>
                  <ListItemText primary="Ver filme" />
                  <ListItemButton>
                    <ListItemIcon>
                      <OpenInNew />
                    </ListItemIcon>
                  </ListItemButton>
                </Link>
              </ListItem>
              <Divider variant="inset" component="li" />
            </>
          ))}
        </List>
      </main>
    </>
  );
}
